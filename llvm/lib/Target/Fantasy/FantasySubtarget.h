#ifndef LLVM_LIB_TARGET_LANAI_LANAISUBTARGET_H
#define LLVM_LIB_TARGET_LANAI_LANAISUBTARGET_H

#include "FantasyFrameLowering.h"
#include "FantasyISelLowering.h"
#include "FantasyInstrInfo.h"
#include "FantasySelectionDAGInfo.h"
#include "llvm/CodeGen/TargetFrameLowering.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/Target/TargetMachine.h"

#define GET_SUBTARGETINFO_HEADER
#include "FantasyGenSubtargetInfo.inc"

namespace llvm {

class FantasySubtarget : public FantasyGenSubtargetInfo {
public:
  // This constructor initializes the data members to match that
  // of the specified triple.
  FantasySubtarget(const Triple &TargetTriple, StringRef Cpu,
                   StringRef FeatureString, const TargetMachine &TM,
                   const TargetOptions &Options, CodeModel::Model CodeModel,
                   CodeGenOpt::Level OptLevel);

  // ParseSubtargetFeatures - Parses features string setting specified
  // subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);

  FantasySubtarget &initializeSubtargetDependencies(StringRef CPU,
                                                    StringRef FS);

  void initSubtargetFeatures(StringRef CPU, StringRef FS);

  bool enableMachineScheduler() const override { return true; }

  const FantasyInstrInfo *getInstrInfo() const override { return &InstrInfo; }

  const TargetFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }

  const FantasyRegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }

  const FantasyTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }

  const FantasySelectionDAGInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }

private:
  FantasyFrameLowering FrameLowering;
  FantasyInstrInfo InstrInfo;
  FantasyTargetLowering TLInfo;
  FantasySelectionDAGInfo TSInfo;
};
} // namespace llvm

#endif // LLVM_LIB_TARGET_LANAI_LANAISUBTARGET_H
